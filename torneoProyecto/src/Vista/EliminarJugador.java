    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

         try {
            int fila= jTable1.getSelectedRow();
            Conexion conn= new Conexion();
            Connection con = conn.getConexion();
            String sql="DELETE FROM Jugador WHERE idJugador="+ jTable1.getValueAt(fila, 0);
            Statement ps= con.createStatement();
            int n = ps.executeUpdate(sql);
            if(n>0){
                llenarFiltro("");
                JOptionPane.showMessageDialog(null,"Jugador eliminado");
                limpiar();
            } 
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error al eliminar jugador");
            limpiar();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnEliminarActionPerformed

